current_weather = 'foggy'

case current_weather
when 'sunny'
    puts "Wear short sleeves, shorts and sunglasses!"
when 'foggy'
    puts "Have a jacket on you, just in case!"
when 'snowy'
    puts "Puffer jackets with snow boots, for sure!"
when 'rainy'
    puts "Wear a raincoat and rain boots, and don't forget an umbrella!"
when 'chilly'
    puts "Perfect time to don that favorite ugly sweater!"
else puts "Hmm, I don't know what you should wear in that kind of weather!"
end